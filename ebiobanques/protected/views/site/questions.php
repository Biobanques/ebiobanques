<div class="faq">
<h3>A quoi sert le projet ebiobanques.fr?</h3>
<div>ebiobanques.fr permet de faciliter la recherche de matériel biologique issues des biobanques.</div>
</div>
<div class="faq">
<h3>A qui est destiné ebiobanques.fr?</h3>
<div>ebiobanques.fr est util pour tout chercheur en médecine/biologie qui souhaite acquérir du matériel biologique dans le cadre de ses études.
Il est aussi destiné à toute biobanque désireuse de valoriser son matériel biologique.</div>
</div>
<div class="faq">
<h3>Qui peut participer pour alimenter la base de données?</h3>
<div>La base de données ebiobanques.fr est alimentée automatiquement par les biobanques qui souhaitent valoriser leur matériel biologique dans un catalogue ouvert aux chercheurs.</div>
</div>
<div class="faq">
<h3>Comment est maintenu ce système?</h3>
<div>La base de données ainsi que le site web ebiobanques.fr sont maintenus par l'infrastructure nationale française  Biobanques.
    Les biobanques participantes au projet possèdent leurs propres outils de gestion de collections d'échantillons biologiques, pour lesquelles un outil d'export automatisé est conçu.
    Pour participer à l'alimentation de ce système, une simple demande auprès de l'infrastructure Biobanques suffit, ensuite un connecteur est adapté au logiciel de la biobanque pour lui permettre d'exporter automatiquement ces informations utiles et autorisées.</div>
</div>